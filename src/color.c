tree_root *colors;

static void color_set (int include_in_palette,
                       const char *name,
                       color *c, uint8_t r, uint8_t g, uint8_t b, uint8_t a)
{
    tree_color_val *val;

    c->r = r;
    c->g = g;
    c->b = b;
    c->a = a;

    if (!colors) {
        colors = tree_alloc(TREE_KEY_STRING, "TREE ROOT: colors");
    }

    val = (typeof(val)) myzalloc(sizeof(*val), "TREE NODE: color");
    val->tree.key = dupstr(name, "TREE KEY: color");
    val->c = *c;
    val->include_in_palette = include_in_palette;

    if (!tree_insert(colors, &val->tree.node)) {
        ERR("insert color %s", name);
    }
}

