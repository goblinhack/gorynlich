color gl_save_color;
color gl_last_color;

void glcolor_save (void)
{
    gl_save_color = gl_last_color;
}

void glcolor_restore (void)
{
    color s = gl_last_color = gl_save_color;

    if (HEADLESS) {
        return;
    }

    glColor4ub(s.r, s.g, s.b, s.a);
}

color string2color (const char **s)
{
    static char tmp[MAXSTR];
    static const char *eo_tmp = tmp + MAXSTR;
    const char *c = *s;
    char *t = tmp;

    while (t < eo_tmp) {
        if ((*c == '\0') || (*c == '$')) {
            break;
        }

        *t++ = *c++;
    }

    if (c == eo_tmp) {
        return (WHITE);
    }

    *t++ = '\0';
    *s += (t - tmp);

    if (!strcasecmp(tmp, "reset")) {
        color c = gl_save_color;
	return (c);
    }

    tree_color_val find;
    tree_color_val *target;

    memset(&find, 0, sizeof(find));
    find.tree.key = tmp;

    target = (typeof(target)) tree_find(colors, &find.tree.node);
    if (!target) {
        ERR("Unknown color [%s]", tmp);
        return (WHITE);
    }

    return (target->c);
}

